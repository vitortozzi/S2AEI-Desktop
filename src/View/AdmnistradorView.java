    private void bttnEditAdmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnEditAdmActionPerformed

        int rowIndex = tabelaAdministradores.getSelectedRow();

        if (rowIndex == -1) {
            JOptionPane.showMessageDialog(null,"Escolha um Administrador da tabela para editar.", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {

            CardLayout content = (CardLayout) (admContentPanel.getLayout());
            content.show(admContentPanel, "admEdit");

            String emailAdmSelecionado = (String) tabelaAdministradores.getModel().getValueAt(rowIndex, 1);
            Administrador adm;
            admController = new AdministradorController();

            adm = admController.getAdmByEmail(emailAdmSelecionado);

            admNomeEdit.setText(adm.getNome());
            admEmailEdit.setText(adm.getEmail());
            admSenhaEdit.setText(adm.getSenha());
            admStatusEdit.setSelectedItem(adm.getStatus());
        }
    }//GEN-LAST:event_bttnEditAdmActionPerformed

