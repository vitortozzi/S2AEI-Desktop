    private void bttnConfirmAddAdmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnConfirmAddAdmActionPerformed

        boolean check = false;

        admController = new AdministradorController();
        check = admController.addAdministrador(admName.getText(), admEmail.getText(), admPassword.getText());

        if (check) {
            JOptionPane.showMessageDialog(null, "Administrador adicionado com sucesso!");

            CardLayout content = (CardLayout) (admContentPanel.getLayout());
            content.show(admContentPanel, "admIndex");

            // atualizar tabela
            admController = new AdministradorController();
            tabelaAdministradores = admController.updateTable(tabelaAdministradores);
        } else {
            JOptionPane.showMessageDialog(null, "Não foi possível completar a ação. Verifique se todos "
                + "os campos estão preenchidos (obrigatório).", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bttnConfirmAddAdmActionPerformed

