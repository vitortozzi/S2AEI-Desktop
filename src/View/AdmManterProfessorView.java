    private void bttnEditProfessorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnEditProfessorActionPerformed

        int rowIndex = tabelaProfessores.getSelectedRow();

        if (rowIndex == -1) {
            JOptionPane.showMessageDialog(null,"Escolha um Professor da tabela para editar.", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {
        
            CardLayout content = (CardLayout) (professorContentPanel.getLayout());
            content.show(professorContentPanel, "professorEdit");
            
            String emailProfSelecionado = (String) tabelaProfessores.getModel().getValueAt(rowIndex, 1);
            Professor p;
            profController = new ProfessorController();
            
            p = profController.getProfessorByEmail(emailProfSelecionado);
            
            profNomeEdit.setText(p.getNome());
            profEmailEdit.setText(p.getEmail());
            profSenhaEdit.setText(p.getSenha());
            profStatusEdit.setSelectedItem(p.getStatus());
            profDepartEdit.setText(p.getDepartamento());
            profDiscEdit.setText(p.getDisciplinaPrincipal());
        }

    }//GEN-LAST:event_bttnEditProfessorActionPerformed

