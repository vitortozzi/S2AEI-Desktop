    private void bttnDeleteProfessorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnDeleteProfessorActionPerformed
        CardLayout content = (CardLayout) (professorContentPanel.getLayout());
        content.show(professorContentPanel, "professorDelete");
        
        int rowIndex = tabelaProfessores.getSelectedRow();

        if (rowIndex == -1) {
            JOptionPane.showMessageDialog(null,"Escolha um Professor da tabela para excluir.", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {
            
            String message = "";
            String emailProfSelec = (String) tabelaProfessores.getModel().getValueAt(rowIndex, 1);
            String nomeProfSelec = (String) tabelaProfessores.getModel().getValueAt(rowIndex, 0);
            String departamentoProfSelec = (String) tabelaProfessores.getModel().getValueAt(rowIndex, 3);
            String discPrincipalProfSelec = (String) tabelaProfessores.getModel().getValueAt(rowIndex, 4);
            
            String temp = "Deseja excluir o seguinte Professor:\n";
            temp += "\n\t\tNome: " + nomeProfSelec;
            temp += "\n\t\tEmail: " + emailProfSelec;
            temp += "\n\t\tDepartamento: " + departamentoProfSelec;
            temp += "\n\t\tDisciplina principal: " + discPrincipalProfSelec;
            temp += "\n\nA exclusão do Professor o tornará 'Inativo', incapacitando-o de orientar de projetos";

            int escolha = JOptionPane.showConfirmDialog(null, temp, "Confirmar exclusão?", JOptionPane.OK_CANCEL_OPTION, 0, null);
            
            if (escolha == JOptionPane.OK_OPTION) {
            
                profController = new ProfessorController();
                message = profController.delProfessor(emailProfSelec);
            
                JOptionPane.showMessageDialog(null, message);
                
                // atualizar tabela
                profController = new ProfessorController();
                tabelaProfessores = profController.updateTable(tabelaProfessores);
            }
        }
    }//GEN-LAST:event_bttnDeleteProfessorActionPerformed

